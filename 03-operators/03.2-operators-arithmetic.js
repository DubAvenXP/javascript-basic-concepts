// Remainder
// Binary operator. Returns the integer remainder 
// of dividing the two operands.
// Syntax: a % b
console.log(5 % 2);

// Increment
// Unary operator. Returns the value of its operand
// incremented by 1.
// Syntax: ++a
let a = 1;
console.log(++a);

// Decrement
// Unary operator. Returns the value of its operand
// decremented by 1.
// Syntax: --a
let b = 1;
console.log(--b);

// Unary plus
// Unary operator. Converts its operand to a number.
// Syntax: +a
let c = "1";
console.log(+c);

// Unary negation
// Unary operator. Converts its operand to a number and converts
// any positive number to a negative number and vice versa.
// and negates it.
// Syntax: -a
let d = "1";
let r = -2;
let s = 3;

console.log(-r);
console.log(-s);
console.log(-d);


// Exponentiation
// Binary operator. Returns the result of raising the first operand
// to the power of the second operand.
// Syntax: a ** b
let e = 2;
let f = 3;
console.log(e ** f);


