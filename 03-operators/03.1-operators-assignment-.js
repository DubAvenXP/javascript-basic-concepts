
// Assignment operators

// Assignment 
let x = 10;

// Addition assignment
x += 5;

// Subtraction assignment
x -= 5;

// Multiplication assignment
x *= 5;

// Division assignment	
x /= 5;

// Remainder assignment	
x %= 5;

// Exponentiation assignment
x **= 5;


let mySecretVar = false;


// Logical OR assignment
let logicalOrExample = mySecretVar || 10;

// short hand
mySecretVar ||= 10;

console.log(mySecretVar);
console.log(logicalOrExample);





let mySecretVar2 = false;

// Logical AND assignment
let logicalAndExample = mySecretVar2 && 10;

// short hand
mySecretVar2 &&= 10;

console.log(mySecretVar2);
console.log(logicalAndExample);
