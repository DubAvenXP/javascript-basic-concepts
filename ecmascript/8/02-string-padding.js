const myString = 'Hello';

console.log(myString);

// padStart adds characters to the beginning of the string until it reaches the specified length
console.log(myString.padStart(10, '_'));

// padEnd adds characters to the end of the string until it reaches the specified length
console.log(myString.padEnd(10, '_'));


// Other examples

const laugth = '';

console.log(laugth.padStart(10, 'ha'));

